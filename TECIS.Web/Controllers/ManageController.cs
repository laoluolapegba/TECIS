        private IEnumerable<Notification> GetUserNotifications()
        {
            // get the user ID
            var userId = User.Identity.Name; // GetUserId();

            //var notifications = null;
            // load our notifications
            var _context = new TecIsEntities();
            //!System.Convert.ToBoolean((int)n.ISDISMISSED)
            var notifications = _context.Notifications
                .Where(n => n.UserId == userId)
                .Where(n => !n.IsDismissed)
                .Select(n => n)
                .ToList().AsEnumerable();
            return notifications;
        }

        [HttpPost]
        public ActionResult MarkNotificationAsRead(int id)
        {
            var userNotification = GetUserNotifications().FirstOrDefault(n => n.NoteId == id);

            if (userNotification == null)
            {
                return new HttpNotFoundResult();
            }

            userNotification.IsDismissed = true;
            _context.SaveChanges();

            return RedirectToAction("Manage");
        }

        public ActionResult GetNotification(int id)
        {
            var userNotification = GetUserNotifications().FirstOrDefault(n => n.NoteId == id);

            if (userNotification == null)
            {
                return new HttpNotFoundResult();
            }

            return PartialView("_RenderNotifications.ModalPreview", userNotification);
        }
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Avatar(HttpPostedFileBase upload)
        {
            try
            {
                if (ModelState.IsValid)
                {
                    File avatar = null;
                    string usrId = User.Identity.GetUserId();
                    int count = db.Files.Count(a => a.UserId == usrId);

                    if (upload != null && upload.ContentLength > 0)
                    {
                        if (count > 0)
                        {
                            File CurrentAvatar = db.Files.Where(s => s.UserId == usrId).FirstOrDefault();
                            //File CurrentAvatar = db.Files.Find(1);
                            db.Files.Remove(CurrentAvatar);
                        }
                        avatar = new File
                        {
                            FileName = System.IO.Path.GetFileName(upload.FileName),
                            FileType = FileType.Avatar,
                            ContentType = upload.ContentType,
                            UserId = User.Identity.GetUserId()
                        };
                        using (var reader = new System.IO.BinaryReader(upload.InputStream))
                        {
                            avatar.Content = reader.ReadBytes(upload.ContentLength);
                        }
                        //student.Files = new List<File> { avatar };
                        //File f = new File { avatar };
                    }
                    db.Files.Add(avatar);
                    db.SaveChanges();
                    return RedirectToAction("Profile");
                }
            }
            catch (RetryLimitExceededException /* dex */)
            {
                //Log the error (uncomment dex variable name and add a line here to write a log.
                ModelState.AddModelError("", "Unable to save changes. Try again, and if the problem persists see your system administrator.");
            }
            return View("Profile");
        }
        private bool IsImage(HttpPostedFileBase file)
        {
            if (file == null) return false;
            return file.ContentType.Contains("image") ||
                _imageFileExtensions.Any(item => file.FileName.EndsWith(item, System.StringComparison.OrdinalIgnoreCase));
        }
